// Славянское правило плюрализования (1 / 2-4 / 5+). Общее для русского UI обеих дорожек.
function _ruPlural(n, one, few, many){
  const a=Math.abs(n)%100, l=a%10;
  return l===1&&a!==11 ? one : (l>=2&&l<=4&&(a<10||a>=20)) ? few : many;
}
